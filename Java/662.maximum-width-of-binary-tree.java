/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    public int widthOfBinaryTree(TreeNode root) {
        if (root == null) return 0;
        Map<TreeNode, Integer> map = new HashMap<>();
        Queue<TreeNode> q = new LinkedList<>();
        q.offer(root);
        map.put(root, 1);
        int maxW = 0;
        while (!q.isEmpty()) {
            int size = q.size();
            int l = 0, r = 0;
            for (int i = 0; i < size; i++) {
                TreeNode cur = q.poll();
                if (cur.left != null) {
                    q.offer(cur.left);
                    map.put(cur.left, map.get(cur) * 2);
                }
                if (cur.right != null) {
                    q.offer(cur.right);
                    map.put(cur.right, map.get(cur) * 2 + 1);
                }
                if (i == 0) l = map.get(cur);
                if (i == size - 1) r = map.get(cur);
            }
            maxW = Math.max(maxW, r - l + 1);
        }
        return maxW;
    }
}
